#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	if (!src && !dest)
		return (NULL);
	d = dest;
	s = src;
	while (n--)
		d[n] = s[n];
	return (dest);
}
