#include "libft.h"

int	ft_isnumber(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (0);
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			i++;
		else 
			return (1);
	}
	return (0);
}